valor1 = float(input("Digite o primeiro numero: "))
valor2 = float(input("Digite o segundo numero: "))

resp = valor1 + valor2
print(f"{valor1} + {valor2} = {resp:.1f}")

resp = valor1 - valor2
print(f"{valor1} - {valor2} = {resp:.1f}")

resp = valor1 * valor2
print(f"{valor1} * {valor2} = {resp:.1f}")

resp = valor1 / valor2
print(f"{valor1} / {valor2} = {resp:.1f}")

resp = valor1 ** valor2
print(f"{valor1} ** {valor2} = {resp:.1f}")

resp = valor1 // valor2
print(f"{valor1} // {valor2} = {resp:.1f}")

resp = valor1 % valor2
print(f"{valor1} % {valor2} = {resp:.1f}")